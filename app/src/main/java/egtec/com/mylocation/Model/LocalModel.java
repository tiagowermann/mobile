package egtec.com.mylocation.Model;

public class LocalModel {

    private int id;
    private int id_rota;
    private String locais;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getId_rota() {
        return id_rota;
    }

    public void setId_rota(int id_rota) {
        this.id_rota = id_rota;
    }

    public String getLocais() {
        return locais;
    }

    public void setLocais(String locais) {
        this.locais = locais;
    }
}
